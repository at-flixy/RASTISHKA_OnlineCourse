export async function generateMetadata(): Promise<Metadata> {
  await connection();
  const page = await db.page.findUnique({ where: { slug: "offer" } });
  return {
    title: page?.seoTitle ?? "Публичная оферта | " + legalEntity.brand,
    description:
      page?.seoDesc ??
      "Публичная оферта на оказание образовательных услуг в дистанционном формате.",
  };
}

